<script>
export default {
  name: "App",
  data() {
    return {
      email: "",
      password: "",
      remember: false,
    };
  },
  created() {
    this.email = localStorage.getItem("email") || "";
  },
  methods: {
    handleLogin() {
      console.log(this.email, this.password, this.remember);
      if (!this.isValidEmail()) {
        alert("Please enter a valid email address.");
      } else if (!this.isValidPassword()) {
        alert(
          "Password must be at least 4 characters long and contain at least one letter and one number."
        );
      } else {
        alert("Form submitted successfully!");
      }
    },
    isValidEmail() {
      const emailRegex = /^[^\s@]+@[^\s@]+\.[^\s@]+$/;
      if (!emailRegex.test(this.email)) {
        return false;
      }
      return true;
    },
    isValidPassword() {
      const passwordRegex =
        /^(?=.*[A-Z])(?=.*[a-z])(?=.*[!@#$%^&*()_+=[\]{};':"\\|,.<>?/`~\-])(?=.{4,})/;
      if (!passwordRegex.test(this.password)) {
        return false;
      }
      return true;
    },
    isRemember() {
      if (!this.remember) {
        alert("Remember me is checked!");
        // 로컬 스토리지 저장
        localStorage.setItem("email", this.email);
      } else {
      }
    },
  },
  computed: {
    isFormValid() {
      return this.email.trim() !== "" && this.password.trim() !== "";
    },
  },
};
</script>

<template>
  <div class="flex items-center justify-center min-h-screen bg-gray-900">
    <div
      class="w-full max-w-sm p-4 bg-white border border-gray-200 rounded-lg shadow sm:p-6 md:p-8"
    >
      <form class="space-y-6" action="#" @submit.prevent="handleLogin">
        <h5 class="text-xl font-medium text-gray-900">
          Sign in to our platform
        </h5>
        <div>
          <label
            for="email"
            class="block mb-2 text-sm font-medium text-gray-900"
            >Your email</label
          >
          <input
            type="email"
            name="email"
            id="email"
            class="bg-gray-50 border border-gray-300 text-gray-900 text-sm rounded-lg focus:ring-blue-500 focus:border-blue-500 block w-full p-2.5"
            placeholder="name@company.com"
            autocomplete="off"
            v-model="email"
            required
          />
        </div>
        <div>
          <label
            for="password"
            class="block mb-2 text-sm font-medium text-gray-900"
            >Your password</label
          >
          <input
            type="password"
            name="password"
            id="password"
            placeholder="••••••••"
            class="bg-gray-50 border border-gray-300 text-gray-900 text-sm rounded-lg focus:ring-blue-500 focus:border-blue-500 block w-full p-2.5"
            v-model="password"
            required
          />
        </div>
        <div class="flex items-start">
          <div class="flex items-start">
            <div class="flex items-center h-5">
              <input
                id="remember"
                type="checkbox"
                value=""
                class="w-4 h-4 border border-gray-300 rounded bg-gray-50 focus:ring-3 focus:ring-blue-300"
                v-model="remember"
                @click="isRemember"
              />
            </div>
            <label for="remember" class="text-sm font-medium text-gray-900 ms-2"
              >Remember me</label
            >
          </div>
          <a href="#" class="text-sm text-blue-700 ms-auto hover:underline"
            >Lost Password?</a
          >
        </div>
        <button
          type="submit"
          class="w-full text-white bg-blue-700 hover:bg-blue-800 focus:ring-4 focus:outline-none focus:ring-blue-300 font-medium rounded-lg text-sm px-5 py-2.5 text-center disabled:bg-gray-300 disabled:cursor-not-allowed"
          :disabled="!isFormValid"
        >
          Login to your account
        </button>
        <div class="text-sm font-medium text-gray-500">
          Not registered?
          <a href="#" class="text-blue-700 hover:underline">Create account</a>
        </div>
      </form>
    </div>
  </div>
</template>

