<script>
export default {
  data() {
    return {
      email: "",
      password: "",
      rememberMe: false,
    };
  },
  methods: {
    isValidated() {
      //이메일 정규식
      const emailCheck = /^[^\s@]+@[^\s@]+\.[^\s@]+$/;
      //비밀번호 정규식
      const passwordCheck =
        /^(?=.*[A-Za-z])(?=.*\d)(?=.*[@$!%*?&])[A-Za-z\d@$!%*?&]{4,}$/;

      if (!emailCheck.test(this.email)) {
        alert("입력된 이메일이 올바른 이메일 형식인지 확인하세요");
        return false;
      }
      if (!passwordCheck.test(this.password)) {
        alert(
          "비밀번호는 대/소문자/특수기호 1개 포함 및 4자 이상인지 확인하세요."
        );
        return false;
      }
      if (this.rememberMe) {
        alert("Remember me is checked!");
        localStorage.setItem("savedEmail", this.email);
        localStorage.setItem("rememberMe", true);
      } else {
        localStorage.removeItem("savedEmail");
        localStorage.removeItem("rememberMe");
      }
      alert("Form submitted successfully!");
      return true;
    },
  },

  computed: {
    isDisabled() {
      return !(this.email || this.password);
    },
  },

  mounted() {
    const rememberMe = localStorage.getItem("rememberMe");
    if (rememberMe) {
      this.email = localStorage.getItem("savedEmail");
      this.rememberMe = true;
    }
  },
};
</script>
<template>
  <div class="flex items-center justify-center min-h-screen bg-gray-900">
    <div
      class="w-full max-w-sm p-4 bg-white border border-gray-200 rounded-lg shadow sm:p-6 md:p-8"
    >
      <form class="space-y-6" action="#">
        <h5 class="text-xl font-medium text-gray-900">
          Sign in to our platform
        </h5>
        <div>
          <label
            for="email"
            class="block mb-2 text-sm font-medium text-gray-900"
            >Your email</label
          >
          <input
            v-model="email"
            type="email"
            name="email"
            id="email"
            class="bg-gray-50 border border-gray-300 text-gray-900 text-sm rounded-lg focus:ring-blue-500 focus:border-blue-500 block w-full p-2.5"
            placeholder="name@company.com"
            autocomplete="off"
            required
          />
        </div>
        <div>
          <label
            for="password"
            class="block mb-2 text-sm font-medium text-gray-900"
            >Your password</label
          >
          <input
            v-model="password"
            type="password"
            name="password"
            id="password"
            placeholder="••••••••"
            class="bg-gray-50 border border-gray-300 text-gray-900 text-sm rounded-lg focus:ring-blue-500 focus:border-blue-500 block w-full p-2.5"
            required
          />
        </div>
        <div class="flex items-start">
          <div class="flex items-start">
            <div class="flex items-center h-5">
              <input
                v-model="rememberMe"
                id="remember"
                type="checkbox"
                value=""
                class="w-4 h-4 border border-gray-300 rounded bg-gray-50 focus:ring-3 focus:ring-blue-300"
              />
            </div>
            <label for="remember" class="ms-2 text-sm font-medium text-gray-900"
              >Remember me</label
            >
          </div>
          <a href="#" class="ms-auto text-sm text-blue-700 hover:underline"
            >Lost Password?</a
          >
        </div>
        <button
          type="submit"
          class="w-full text-white bg-blue-700 hover:bg-blue-800 focus:ring-4 focus:outline-none focus:ring-blue-300 font-medium rounded-lg text-sm px-5 py-2.5 text-center disabled:bg-gray-300 disabled:cursor-not-allowed"
          :disabled="isDisabled"
          @click="isValidated"
        >
          Login to your account
        </button>
        <div class="text-sm font-medium text-gray-500">
          Not registered?
          <a href="#" class="text-blue-700 hover:underline">Create account</a>
        </div>
      </form>
    </div>
  </div>
</template>

